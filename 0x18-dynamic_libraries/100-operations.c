/**
 * add - adds.
 * @a: int.
 * @b: int.
 * Return: ...
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts.
 * @a: int.
 * @b: int.
 * Return: ...
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies.
 * @a: int.
 * @b: int.
 * Return: ...
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides.
 * @a: int.
 * @b: int.
 * Return: ...
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - calculate modul.
 * @a: int.
 * @b: int.
 * Return: ...
 */
int mod(int a, int b)
{
	return (a % b);
}
